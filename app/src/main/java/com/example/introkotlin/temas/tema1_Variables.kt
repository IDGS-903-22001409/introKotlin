package com.example.introkotlin.temasKotlin

fun main(){
    // Varables Val
    val nombre = "Sergio"
    val edad = 20

    println("Hello $nombre tu edad es $edad")
    println("Hola mundo")

    // Variables Var
    var num1 = 1
    var num2 = 10

    // Operaciones con Var
    var suma = num1 + num2
    var resta = num1 - num2
    var multiplicacion = num1 * num2
    var division = num1 / num2

    // Var y Val la diferencia es que Val es una constante y no es remplazable en su valor
    println("La suma es $num1 + $num2 es = $suma")
    println("La suma es $num1 - $num2 es = $resta")
    println("La suma es $num1 x $num2 es = $multiplicacion")
    println("La suma es $num1 / $num2 es = $division")
}
